const trigger = document.getElementById('trigger');
const dropdown = document.getElementById('dropdown');
const optionsList = document.getElementById('optionsList');
const selectedTextEl = document.getElementById('selectedText');
const enterBtn = document.getElementById('enterBtn');
const loginScreen = document.getElementById('login-screen');
const contentScreen = document.getElementById('content-screen');

function buildOptions() {
  optionsList.innerHTML = '';
  for (const code in products) {
    const li = document.createElement('li');
    li.className = 'custom-option';
    li.textContent = products[code].nome + ' (' + code + ')';
    li.addEventListener('click', () => {
      selectedTextEl.textContent = li.textContent;
      enterBtn.dataset.code = code;
      dropdown.style.display = 'none';
    });
    optionsList.appendChild(li);
  }
}
buildOptions();

trigger.addEventListener('click', () => {
  dropdown.style.display = dropdown.style.display === 'block' ? 'none' : 'block';
});

enterBtn.addEventListener('click', () => {
  const code = enterBtn.dataset.code;
  if (!code) return alert('Selecione um produto!');
  const produto = products[code];
  loginScreen.style.display = 'none';
  contentScreen.style.display = 'block';
  contentScreen.innerHTML = '<h2>' + produto.nome + '</h2>' +
    produto.tutorial.map(sec =>
      '<div class="section"><h3><i class="fa ' + sec.icon + '"></i>' + sec.titulo + '</h3><ol>' +
      sec.passos.map(p => '<li>' + p + '</li>').join('') +
      '</ol></div>'
    ).join('') +
    '<div class="notice"><i class="fa-solid fa-comment-dots"></i> Precisa de ajuda? Vá até o <b>Discord oficial</b>.</div>' +
    '<button class="btn" onclick="location.reload()">⬅ Voltar</button>';
});

document.addEventListener('click', e => {
  if (!trigger.contains(e.target)) dropdown.style.display = 'none';
});
